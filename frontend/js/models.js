class libro {
    constructor(id, id_genero_literario, nombre, sinopsis, id_portada, portada, precio, impuesto,
        stock, editorial, edicion, fecha_publicacion, idioma, numero_paginas, coleccion){
        this.id = id;
        this.id_genero_literario = id_genero_literario;
        this.nombre = nombre;
        this.sinopsis = sinopsis;
        this.id_portada = id_portada;
        this.portada = portada;
        this.precio = precio;
        this.impuesto = impuesto;
        this.stock = stock;
        this.editorial = editorial;
        this.edicion = edicion;
        this.fecha_publicacion = fecha_publicacion;
        this.idioma = idioma;
        this.numero_paginas = numero_paginas;
        this.coleccion = coleccion;
    }
}